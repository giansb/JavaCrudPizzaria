    private float CalcularTotal(){
        
        switch(pizzanome){
            case "calabresa":
                precopizza = 45;
                break;
            case "4 queijos":
                precopizza = 50;
                break;
            case "camarão":
                precopizza = 55;
                break;
            case "coração":
                precopizza = 60;
                break;
            case "strogonoff":
                precopizza = 65;
                break;
        }
        float total;
        total = ((precopizza * tampizza)+acompanhamento);
        return total;
        
    }
