    private void botaoImprimirPedidoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botaoImprimirPedidoActionPerformed
        float totalpedido = 0;
        try{
            FileWriter fw = new FileWriter("arq.txt");
            fw.write("\n"+"========== PEDIDO ==========");
            fw.write("\n"+"Cliente: "+c.getNome());
            fw.write("\n"+"Endereço: "+c.getEndereco());
            fw.write("\n"+"Bairro: "+c.getBairro());
            fw.write("\n"+"============================");
            fw.write("\n"+"========== ITENS ==========");
            for(int x =0;x<cont;x++){
            fw.write("\n"+"PIZZA: " +  VetPedido[x].getPizza());
            fw.write("\n"+"TAMANHO: "+ VetPedido[x].getTamanho());
            fw.write("\n"+"Observação: " + VetPedido[x].getOb());
            fw.write("\n"+"VALOR: R$"+VetPedido[x].getPreco());
            fw.write("\n"+"--------------------------");
            totalpedido += VetPedido[x].getPreco();
        }
            fw.write("\n"+"TOTAL DO PEDIDO: "+totalpedido);
            fw.close();
        }catch(Exception e){
            System.out.println("Erro: "+ e.getMessage());
        }
    }//GEN-LAST:event_botaoImprimirPedidoActionPerformed

